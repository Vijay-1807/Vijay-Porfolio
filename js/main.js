// Mobile Navigation Toggle
const navbarToggler = document.querySelector('.navbar-toggler');
const navbarCollapse = document.querySelector('.navbar-collapse');

if (navbarToggler && navbarCollapse) {
  navbarToggler.addEventListener('click', () => {
    navbarCollapse.classList.toggle('show');
  });

  // Close mobile menu when clicking outside
  document.addEventListener('click', (e) => {
    if (!navbarToggler.contains(e.target) && !navbarCollapse.contains(e.target)) {
      navbarCollapse.classList.remove('show');
    }
  });
}

// Active Navigation Link
const sections = document.querySelectorAll('section[id]');
const navLinks = document.querySelectorAll('.nav-link');

function highlightNavigation() {
  const scrollY = window.pageYOffset;

  sections.forEach(section => {
    const sectionHeight = section.offsetHeight;
    const sectionTop = section.offsetTop - 50;
    const sectionId = section.getAttribute('id');

    if (scrollY > sectionTop && scrollY <= sectionTop + sectionHeight) {
      navLinks.forEach(link => {
        link.classList.remove('active');
        if (link.getAttribute('href') === `#${sectionId}`) {
          link.classList.add('active');
        }
      });
    }
  });
}

window.addEventListener('scroll', highlightNavigation);

// Animate elements on scroll
const animateOnScroll = () => {
  const elements = document.querySelectorAll('.animate-on-scroll');
  
  elements.forEach(element => {
    const elementPosition = element.getBoundingClientRect().top;
    const screenPosition = window.innerHeight;
    
    if (elementPosition < screenPosition) {
      element.classList.add('animated');
    }
  });
};

window.addEventListener('scroll', animateOnScroll);

// Form Validation
const contactForm = document.querySelector('#contactForm');
if (contactForm) {
  contactForm.addEventListener('submit', (e) => {
    e.preventDefault();
    
    // Basic form validation
    const name = contactForm.querySelector('#name');
    const email = contactForm.querySelector('#email');
    const message = contactForm.querySelector('#message');
    
    if (name.value.trim() === '') {
      showError(name, 'Name is required');
    } else {
      removeError(name);
    }
    
    if (email.value.trim() === '') {
      showError(email, 'Email is required');
    } else if (!isValidEmail(email.value)) {
      showError(email, 'Please enter a valid email');
    } else {
      removeError(email);
    }
    
    if (message.value.trim() === '') {
      showError(message, 'Message is required');
    } else {
      removeError(message);
    }
    
    // If no errors, submit form
    if (!contactForm.querySelector('.error-message')) {
      // Add your form submission logic here
      console.log('Form submitted successfully');
    }
  });
}

function showError(input, message) {
  const formControl = input.parentElement;
  const errorMessage = formControl.querySelector('.error-message') || document.createElement('small');
  errorMessage.className = 'error-message text-danger';
  errorMessage.innerText = message;
  
  if (!formControl.querySelector('.error-message')) {
    formControl.appendChild(errorMessage);
  }
  
  input.classList.add('is-invalid');
}

function removeError(input) {
  const formControl = input.parentElement;
  const errorMessage = formControl.querySelector('.error-message');
  
  if (errorMessage) {
    formControl.removeChild(errorMessage);
  }
  
  input.classList.remove('is-invalid');
}

function isValidEmail(email) {
  const re = /^(([^<>()\[\]\\.,;:\s@"]+(\.[^<>()\[\]\\.,;:\s@"]+)*)|(".+"))@((\[[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}])|(([a-zA-Z\-0-9]+\.)+[a-zA-Z]{2,}))$/;
  return re.test(String(email).toLowerCase());
}

